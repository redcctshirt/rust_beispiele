fn main() {
    
    // Primitive Datentypen
    
    // Booleans - true, false
    let a: bool = true;
    println!("Booleans: {}", a);

    // char - ein Zeichen (4 Bytes)
    let b = 'A';
    println!("Char: {}", b);

    // numerisch - i vorzeichenbehaftet u vorzeichenlos 8,16,32,64 Bits
    // f32, f64 - float    
    let c: i32 = 23;
    let d: f32 = 23.42;
    println!("Numerisch: {}, {}", c, d);

}
